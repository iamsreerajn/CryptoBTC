        /// <summary>
        /// Method is to Get coin data for last 31 days
        /// </summary>
        /// <param name="fromDate"></param>
        /// <param name="toDate"></param>
        /// <returns></returns>
        public async Task<List<CandlesBTC>> GetCoinCandles(DateTime? fromDate, DateTime? toDate )
        {
            try
            {
                var data = JsonConvert.DeserializeObject<List<CandlesBTC>>(GetCoinResponse());
                data = data.FindAll(c => c.TimestampISO.Date >= fromDate && c.TimestampISO.Date <= toDate);
                return data;
            }
            catch (Exception ex)
            {
                return null; 
            }
        }
        /// <summary>
        /// method to Get Average coin data between from and to dates
        /// </summary>
        /// <param name="fromDate"></param>
        /// <param name="toDate"></param>
        /// <returns></returns>
        public double GetAvgCoinCandles(DateTime fromDate, DateTime toDate)
        {
            try
            {
                var data = JsonConvert.DeserializeObject<List<CandlesBTC>>(GetCoinResponse());
                var result = data.FindAll(obj => obj.TimestampISO.Date >= fromDate.Date && obj.TimestampISO.Date <= toDate.Date);
                double sum = 0;
                if (result.Count > 0)
                {
                    foreach (var item in result)
                    {
                        sum += (double)item.Close;
                    }
                    return sum / result.Count;
                }
                else
                {
                    return 0;
                }
            }
            catch (Exception ex)
            {
                return 0;
            }
        }

